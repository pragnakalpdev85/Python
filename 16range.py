#range
#range with one argument
for i in range(6):
    print(i)

#range with two argument start and stop
for i in range(1,6):
    print(i)

#range with 3 argument start stop and step
for i in range(0,11,2):
    print(i)

#list to display range
list1 = list(range(5))
print(list1)

#slicing on range
range1 = range(10)
print(range1[1:5])